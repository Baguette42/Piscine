int	ft_atoi(char *str)
{
	int	nb;
	int	sign;

	nb = 0;
	sign = 1;
	while (*str == 32 || (*str > 8 && *str < 14))
	{
		str++;
	}
	while (*str == '-' || *str == '+')
	{
		if (*str++ == '-')
		{
			sign = sign * -1;
		}
	}
	while (*str > 47 && *str < 58)
	{
		nb = nb * 10 + (*str - '0');
		str++;
	}
	return (nb * sign);
}
