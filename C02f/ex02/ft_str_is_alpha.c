int	ft_str_is_alpha(char *str)
{
	int	i;
	int	f;

	i = 0;
	f = 1;
	while (str[i])
	{
		if (!(str[i] < 91 && str[i] > 64) && !(str[i] > 96 && str[i] < 123))
		{
			f = 0;
		}
		i++;
	}
	return (f);
}
