int	ft_find_next_prime(int nb)
{
	int	i;

	if (nb < 3)
		return (2);
	if (nb % 2 == 0)
		nb++;
	i = 3;
	while (i * i <= nb)
	{
		if (nb % i == 0)
		{
			nb = nb + 2;
			i = 1;
		}
		i = i + 2;
	}
	return (nb);
}
